package web.Model;

public class Asset {

	private String assetNumber;
	private String assetName;
	private String model;
	private String specification;
	private String totalAsset;
	private String souresFounds;
	private String dateOfAcquisition;
	private String useDepartment;
	private String warehouseman;
	public String getAssetNumber() {
		return assetNumber;
	}
	public void setAssetNumber(String assetNumber) {
		this.assetNumber = assetNumber;
	}
	public String getAssetName() {
		return assetName;
	}
	public void setAssetName(String assetName) {
		this.assetName = assetName;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getSpecification() {
		return specification;
	}
	public void setSpecification(String specification) {
		this.specification = specification;
	}
	public String getTotalAsset() {
		return totalAsset;
	}
	public void setTotalAsset(String totalAsset) {
		this.totalAsset = totalAsset;
	}
	public String getSouresFounds() {
		return souresFounds;
	}
	public void setSouresFounds(String souresFounds) {
		this.souresFounds = souresFounds;
	}
	public String getDateOfAcquisition() {
		return dateOfAcquisition;
	}
	public void setDateOfAcquisition(String dateOfAcquisition) {
		this.dateOfAcquisition = dateOfAcquisition;
	}
	public String getUseDepartment() {
		return useDepartment;
	}
	public void setUseDepartment(String useDepartment) {
		this.useDepartment = useDepartment;
	}
	public String getWarehouseman() {
		return warehouseman;
	}
	public void setWarehouseman(String warehouseman) {
		this.warehouseman = warehouseman;
	}
	
}
